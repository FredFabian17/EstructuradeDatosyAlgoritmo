def suma (a,b):
    resultado = a + b
    return resultado
def resta (a,b):
    resultado = a - b
    return resultado
def multiplicar (a,b):
    resultado = a * b
    return resultado
def dividir (a,b):
    resultado = a / b
    return resultado
def potencia (a,b):
    resultado = a ** b
    return resultado
def raiz (a,b):
    resultado = a ** (1/b)
    return resultado


print("suma: ", suma(5,6))
print("resta: ", resta(5,6))
print("multiplicar: ", multiplicar(5,6))
print("dividir: ", dividir(5,6))
print("potencia: ", potencia(5,6))
print("raiz: ", raiz(5,6))

